package HomeWork_08;

/*Реализовать с использованием switch Ваш ребенок принес оценку за контрольную работу по математике.
В школе 12 бальная система. Запишите в переменную случайное число от 0 до 12.
Вы разрешаете ребенку смотреть телевизор не более 60 минут в день. (Оставшееся время не может быть больше 1 часа)
На сегодня у него осталось 45 минут. (записать в переменную)

 Если оценка от 10 до 12 баллов -> Скажите ему, как вы рады и добавьте к оставшемуся на сегодня времени
 1 час от 7 до 9 -> Слова похвалы на экран + ко времени 45 минут от 4 до 6 ->
 Нейтральные слова + ко времени 15 минут 3 -> Огорчитесь. Минус 30 минут ко времени оценка меньше ->
 Трагедия. Сегодня ТВ смотреть запрещено

        Выведите на экран оставшееся на сегодня время для просмотра ТВ

        P.S. Реализовать с использованием switch*/




import java.util.Random;

public class Task_02 {
    public static void main(String[] args) {
        Random random = new Random();
        int grade = random.nextInt(13); // Генерируем случайное число от 0 до 12
        int remainingTime = 45; // Оставшееся время на сегодня

        System.out.println("Сегодня оценка: " + grade);

        switch (grade) {
            case 10:
            case 11:
            case 12:
                System.out.println("Вы очень рады! Похвала!");
                remainingTime += 60; // Добавляем 1 час
                break;
            case 7:
            case 8:
            case 9:
                System.out.println("Слова похвалы на экран.");
                remainingTime += 45; // Добавляем 45 минут
                break;
            case 4:
            case 5:
            case 6:
                System.out.println("Нейтральные слова.");
                remainingTime += 15; // Добавляем 15 минут
                break;
            default:
                System.out.println("Огорчение. Сегодня ТВ смотреть запрещено.");
                remainingTime -= 30; // Отнимаем 30 минут
                break;
        }

        if (remainingTime > 60) {
            remainingTime = 60; // Ограничиваем оставшееся время 60 минутами
        }

        System.out.println("Оставшееся время для просмотра ТВ: " + remainingTime + " минут");
    }
    }
